    void populateRegisterTable() {
        DefaultTableModel model = (DefaultTableModel) registerTabel.getModel();
        model.setRowCount(0);
        Object[] row = new Object[3];        
        for (WorkRequest r : organization.getWorkQueue().getWorkRequestList()) {            
            if (r instanceof ProviderRegisterRequest) {
                row[0] = r;
                row[1] = ((ProviderRegisterRequest) r).getStatus();                
                row[2] = r.getResolveDate();
                model.addRow(row);
            }
        }
    }

