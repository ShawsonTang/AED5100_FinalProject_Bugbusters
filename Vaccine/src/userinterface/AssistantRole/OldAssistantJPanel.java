    private void approveProviderButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_approveProviderButtonActionPerformed
        int selectedRow = registerTabel.getSelectedRow();
        if (selectedRow < 0){
            JOptionPane.showMessageDialog(null,"Please select a row from table first.","Warning",JOptionPane.WARNING_MESSAGE);
            return;
        }
        ProviderRegisterRequest request = (ProviderRegisterRequest) registerTabel.getValueAt(selectedRow, 0);
        request.setStatus("Approved");
        request.getSender().setRegistered(true);
        request.setResolveDate(new Date());
        populateRegisterTable();
    }//GEN-LAST:event_approveProviderButtonActionPerformed

